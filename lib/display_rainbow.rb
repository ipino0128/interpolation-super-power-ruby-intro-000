def display_rainbow(color)
  rainbow = ["Red", "Orange", "Yellow", "Green", "Blue", "Indigo", "Violet"]
  puts "R: #{color[0]}"
  puts "O: #{color[1]}"
  puts "Y: #{color[2]}"
  puts "G: #{color[3]}"
  puts "B: #{color[4]}"
  puts "I: #{color[5]}"
  puts "V: #{color[6]}"
display_rainbow(rainbow)
end
